    private void EntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EntrarActionPerformed
        // TODO add your handling code here:
        String nombre = nom_u.getText();
        Integer codigo =  (int)Codigo_e.getValue();
        String contraseńa = Contraseńa_e.getText();
        
        try {
        if(initSesion(nombre, codigo, contraseńa)){
            JOptionPane.showMessageDialog(this, "Sesion Iniciada", "Bienvenido", JOptionPane.INFORMATION_MESSAGE);
            dispose();
            ShopView menu = new ShopView(this, true);
            menu.setLocationRelativeTo(null);
            menu.setVisible(true);
        }else{
            count++;
            if(count == 3){
                
          JOptionPane.showMessageDialog(this, "3 intentos fallidos", "Datos incorrectos", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            }else{
          JOptionPane.showMessageDialog(this, "Fallo al validar", "Datos incorrectos", JOptionPane.ERROR_MESSAGE);
          limpiarCampos();
                
            }
        } 
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_EntrarActionPerformed

